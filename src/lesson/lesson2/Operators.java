package lesson.lesson2;

public class Operators {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        int result = a + b;
        System.out.println(result);
        a += b; // a = a + b
        System.out.println(a);
        a++; // унарный оператор, т.к. действие происходит над одной переменной
        
    }
}